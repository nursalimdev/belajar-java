public class TipeDataNumber {
    public static void main(String[] args) {
        // Tipe Integer
        byte myByte = 100;
        short myShort = 2000;
        int myInt = 3000;
        long myLong = 40000000;
        long myLong2 = 40000000L;

        // Tipe Floating number
        float myFloat = 30.25f; // atau 20.25F
        double myDouble = 12.234566;

        // Tipe literal
        int myDecimalInt = 100;
        int myHexaInt = 0xA23AC;
        int mybinInt = 0b1100111;

        // Underscore
        int gaji = 15_000_000;
        long saldo = 1_000_000_000;

    }
}

public class OperasiMatematika {
    public static void main(String[] args) {
        int a = 300;
        int b = 50;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}

public class AugmentedAssignment {
    public static void main(String[] args) {
        int a = 100;
        a += 10;
        System.out.println(a);

        a -= 10;
        System.out.println(a);

        a *= 10;
        System.out.println(a);

        a /= 10;
        System.out.println(a);

        a %= 10;
        System.out.println(a);

    }
}
